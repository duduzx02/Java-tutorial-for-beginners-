public class Variaveis {
    public static void main(String[] args) {

        int x = 123;
        double y = 3.14;
        boolean z = true;
        char symbol = '@';
        String name = "Du";

        System.out.println("Hello " + name);

        // Declaração e atribução separadas
        int numero;
        numero = 42;

        // Inicialização
        int novoNumero = 99;

        int idade = 30;
        double altura = 1.80;
        String nome = "Ana";


        System.out.println("Idade: " + idade);
        System.out.println("Altura: " + altura);
        System.out.println("Nome: " + nome);

    }
}
