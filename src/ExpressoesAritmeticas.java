public class ExpressoesAritmeticas {
    public static void main(String[] args) {
        // expressão = operandos e operadores
        // Operandos = valores, variáveis, números e quantidas
        // Operadores = + - * / %

        // Exemplos
        int friends = 10;
        friends = friends + 1;

        System.out.println(friends);

        friends--;
        System.out.println(friends);




    }
}
